void StartHMITask(void *argument)
{
  int flag = 0;
  int position = 0;
  char thresh[32] = "";
  for (;;)
  {
    while (flag == 1)
    {
      char ch = KeyPad_WaitForKeyGetChar(0);
      if (ch == 'B')
      {
        thresh[position] = '\0';
        flag = 0;
        position = 0;
        energyThreshold = atoi(thresh);
        break;
      }
      OLED_WriteChar(ch, Font_7x10, White);
      OLED_UpdateScreen();
      thresh[position] = ch;
      position++;
    }

    char thstr[32];
    sprintf(thstr, "%d", energyThreshold);
    OLED_Fill(Black);
    OLED_SetCursor(1, 1);
    OLED_WriteString("Home", Font_7x10, White);
    OLED_SetCursor(1, 12);
    OLED_WriteString("Set Thresh: ", Font_7x10, White);
    OLED_WriteString(thstr, Font_7x10, White);
    OLED_SetCursor(1, 48);
    OLED_WriteString("Press A to Set", Font_7x10, White);
    OLED_UpdateScreen();
    char ch = KeyPad_WaitForKeyGetChar(0);
    if (ch == 'A')
    {
      OLED_Fill(Black);
      OLED_SetCursor(1, 1);
      OLED_WriteString("Settings", Font_7x10, White);
      OLED_SetCursor(1, 12);
      OLED_WriteString("Set Thresh:", Font_7x10, White);
      OLED_WriteString(thstr, Font_7x10, White);
      OLED_SetCursor(1, 24);
      OLED_WriteString("New Threshold:", Font_7x10, White);
      OLED_SetCursor(1, 48);
      OLED_WriteString("Press B to Confirm", Font_7x10, White);
      OLED_SetCursor(1, 36);
      OLED_UpdateScreen();
      flag = 1;
    }
  }
}

void StartControlTask(void *argument)
{
  for (;;)
  {
    if (currentEnergyValue > energyThreshold)
    {
      Stepper_Rotate_Reverse(30);
      currentEnergyValue -= 200;
    }
    if (energyThreshold > currentEnergyValue)
    {
      Stepper_Rotate(30);
      currentEnergyValue += 200;
    }
    osDelay(1000);
  }
}

